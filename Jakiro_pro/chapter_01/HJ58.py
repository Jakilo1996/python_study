while 1:
    try:
        a = input()
        b = input()
        a = a.strip().split(' ')
        b = b.strip().split(' ')
        b.sort()
        c = b[0:int(a[1])]
        for i in range(len(c)):
            print(int(c[i]),end=' ')
        pass
    except:
        break
