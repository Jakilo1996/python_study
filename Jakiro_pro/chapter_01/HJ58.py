# 输入描述：
# 第一行输入两个整数n和k
# 第二行输入一个整数数组
# 输入：
# 输出描述：
# 输出一个从小到大排序的整数数组
# 命名规则  文件名小写字母+下划线，类名大驼峰，方法、变量名小写字母+下划线连接
# 常量大写，变量和常量用名词、方法用动词
